
/**
 * This is person interface
 */
public interface Person {
	/**
	 * it returns the name of the person
	 */
	public String getName();
	/**
	 * it sets the name of the person
	 * @param Name name of the person
	 */
	public void setName(String Name);
	/**
	 * it returns the surname of the person
	 */
	public String getSurname();
	/**
	 * it sets the Surname of the person
	 * @param Surname Surname of the person
	 */
	public void setSurname(String Surname);
	/**
	 * it returns the mail of the person
	 */
	public String getMail();
	/**
	 * it sets the Mail of the person
	 * @param Mail Mail of the person
	 */
	public void setMail(String Mail);
	/**
	 * it returns the password of the person
	 */
	public String getPassword();
	/**
	 * it sets the Password of the person
	 * @param Password Password of the person
	 */
	public void setPassword(String Password);
}
